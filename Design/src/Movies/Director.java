package Movies;

public class Director {

	private String Dno; //导演编号
	private String Dn;	//导演名
	private String Dsex;	//导演性别
	private String Dcon;	//导演国籍
	private String Dbri;	//导演简介
	private String Dpic;	//导演照片
	private String DBrith;	//导演出生日期
	public String getDno() {
		return Dno;
	}
	public void setDno(String dno) {
		Dno = dno;
	}
	public String getDn() {
		return Dn;
	}
	public void setDn(String dn) {
		Dn = dn;
	}
	public String getDsex() {
		return Dsex;
	}
	public void setDsex(String dsex) {
		Dsex = dsex;
	}
	public String getDcon() {
		return Dcon;
	}
	public void setDcon(String dCon) {
		Dcon = dCon;
	}
	public String getDbri() {
		return Dbri;
	}
	public void setDbri(String dbri) {
		Dbri = dbri;
	}
	public String getDpic() {
		return Dpic;
	}
	public void setDpic(String dpic) {
		Dpic = dpic;
	}
	public String getDBrith() {
		return DBrith;
	}
	public void setDBrith(String dBrith) {
		DBrith = dBrith;
	}
}

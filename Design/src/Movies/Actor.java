package Movies;

public class Actor {

	private String Ano;	//演员编号
	private String An; //演员名
	private String Asex; //演员性别
	private String Acon;	//演员国籍
	private String Abri;	//演员简介
	private String Apic;	//演员照片
	private String ABrith;	//演员出生日期
	public String getAno() {
		return Ano;
	}
	public void setAno(String ano) {
		Ano = ano;
	}
	public String getAn() {
		return An;
	}
	public void setAn(String an) {
		An = an;
	}
	public String getAsex() {
		return Asex;
	}
	public void setAsex(String asex) {
		Asex = asex;
	}
	public String getAcon() {
		return Acon;
	}
	public void setAcon(String acon) {
		Acon = acon;
	}
	public String getAbri() {
		return Abri;
	}
	public void setAbri(String abri) {
		Abri = abri;
	}
	public String getApic() {
		return Apic;
	}
	public void setApic(String apic) {
		Apic = apic;
	}
	public String getABrith() {
		return ABrith;
	}
	public void setABrith(String aBrith) {
		ABrith = aBrith;
	}
}
